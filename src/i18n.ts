export function translate(locale: AppLocale, key: string, params?: Record<string, string | number>) {
  const value = dictionaries[locale][key] ?? dictionaries.ru[key] ?? key;
  if (typeof value === 'function') {
    return value(params);
  }

  return value.replace(/\{(\w+)\}/g, (_, token: string) => String(params?.[token] ?? `{${token}}`));
}

export function useI18n() {
  const locale = useAppStore((state) => state.locale);
  return {
    locale,
    t: (key: string, params?: Record<string, string | number>) => translate(locale, key, params),
  };
}
