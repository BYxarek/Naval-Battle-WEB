$app_lang = 'ru';

function set_app_lang(?string $lang): void
{
    global $app_lang;
    $app_lang = in_array($lang, ['ru', 'en', 'uk'], true) ? $lang : 'ru';
}

function app_lang(): string
{
    global $app_lang;
    return $app_lang;
}

function tr(string $key, array $params = []): string
{
    $lang = app_lang();
    $value = APP_I18N[$lang][$key] ?? APP_I18N['ru'][$key] ?? $key;

    return preg_replace_callback('/\{(\w+)\}/', static function (array $matches) use ($params): string {
        return isset($params[$matches[1]]) ? (string) $params[$matches[1]] : $matches[0];
    }, $value) ?? $value;
}
